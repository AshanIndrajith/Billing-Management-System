    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    

       String productId = id.getText();
    String productName = pname.getText();
    String productRate = rate.getText();
    String productDescription = des1.getText();
    String productActivate = activate.getText();

    try {
        Connection con = ConnectionProvider.getCon();
        Statement st = con.createStatement();

        // Construct the SQL update query
        String query = "UPDATE product SET pName = '" + productName + "', rate = '" + productRate + "', description = '" + productDescription + "', activate = '" + productActivate + "' WHERE pId = '" + productId + "'";

        // Execute the update query
        int rowsUpdated = st.executeUpdate(query);

        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(null, "Product updated successfully");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to update product");
        }

        // Close resources
        st.close();
        con.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }

        
       
      
    

    }//GEN-LAST:event_jButton1ActionPerformed

