    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
   String productId = id.getText();
   System.out.println("Product ID: " + productId);

try {
    Connection con = ConnectionProvider.getCon();
    if (con != null && !con.isClosed()) {
        System.out.println("Database connection established.");
    } else {
        System.out.println("Database connection is null or closed.");
    }
    
    Statement st = con.createStatement();
    ResultSet rs = st.executeQuery("SELECT * FROM product WHERE pId='" + productId + "'");
    
    if (rs.next()) {
        // Set the retrieved details to appropriate fields
        pname.setText(rs.getString("pName")); 
        rate.setText(rs.getString("rate"));
        des1.setText(rs.getString("description"));
        activate.setText(rs.getString("activate"));
        
        // If your 'activate' column is of type boolean, you might need to convert it to yes/no
      
        
        // Disable editing of product ID
        id.setEditable(false);
    } else {
        JOptionPane.showMessageDialog(null, "Product ID does not exist");
    }
    
    // Close resources
    rs.close();
    st.close();
    con.close();
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, e);
}

        
    }//GEN-LAST:event_jButton4ActionPerformed

